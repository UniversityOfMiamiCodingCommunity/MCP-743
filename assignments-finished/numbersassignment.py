#Pythagorean Theorem
#####################

# a is first side of triangle (in)
###########################
a = 3.0

# b is second side of triangle (in)
##############################
b = 4.0

# Set equation equal to c squared (in)
####################################
c_squared = a ** 2 + b ** 2

# Solve for c (in)
##################
c = c_squared ** 0.5

# Print results to screen
####################################
print("Hypotenuse:", c, "inches")

# Kinetic Energy Formula
########################

# Mass in kilograms
###################
m = 5.0

# Velocity in meters/second
###########################
v = 2.0

# Solve for kinetic energy in Joules
####################################
